defmodule UnshackledWeb do
  @moduledoc """
  The entrypoint for defining your web interface, such
  as controllers, components, channels, and so on.

  This can be used in your application as:

      use UnshackledWeb, :controller
      use UnshackledWeb, :live_view

  The definitions below will be executed for every controller,
  component, etc, so keep them short and clean, focused
  on imports, uses and aliases.
  """

  @spec static_paths :: [String.t()]
  def static_paths, do: ~w(assets fonts images favicon.ico robots.txt)

  @spec router :: Macro.t()
  def router do
    quote do
      use Phoenix.Router, helpers: false

      import Plug.Conn
      import Phoenix.Controller
      import Phoenix.LiveView.Router
    end
  end

  @spec channel :: Macro.t()
  def channel do
    quote do
      use Phoenix.Channel
    end
  end

  @spec controller :: Macro.t()
  def controller do
    quote do
      use Phoenix.Controller,
        formats: [:html, :json],
        layouts: [html: UnshackledWeb.Layouts]

      import Plug.Conn

      unquote(verified_routes())
    end
  end

  @spec live_view :: Macro.t()
  def live_view do
    quote do
      use Phoenix.LiveView,
        layout: {UnshackledWeb.Layouts, :app}

      unquote(html_helpers())
    end
  end

  @spec live_component :: Macro.t()
  def live_component do
    quote do
      use Phoenix.LiveComponent

      unquote(html_helpers())
    end
  end

  @spec html :: Macro.t()
  def html do
    quote do
      use Phoenix.Component

      import Phoenix.Controller,
        only: [get_csrf_token: 0, view_module: 1, view_template: 1]

      unquote(html_helpers())
    end
  end

  defp html_helpers do
    quote do
      import Phoenix.HTML
      import UnshackledWeb.CoreComponents

      unquote(verified_routes())
    end
  end

  # HTML helpers without verified routes - for modules that don't use ~p sigil
  # This breaks the compile-time cycle between LiveViews and Endpoint/Router
  defp html_helpers_minimal do
    quote do
      import Phoenix.HTML
      import UnshackledWeb.CoreComponents
    end
  end

  @doc """
  LiveView variant without verified routes to break compile-time cycles.
  Use this for LiveViews that don't need the ~p sigil.
  """
  @spec live_view_minimal :: Macro.t()
  def live_view_minimal do
    quote do
      use Phoenix.LiveView,
        layout: {UnshackledWeb.Layouts, :app}

      unquote(html_helpers_minimal())
    end
  end

  @spec verified_routes :: Macro.t()
  def verified_routes do
    quote do
      use Phoenix.VerifiedRoutes,
        endpoint: UnshackledWeb.Endpoint,
        router: UnshackledWeb.Router,
        statics: UnshackledWeb.static_paths()
    end
  end

  @doc """
  When used, dispatch to the appropriate controller/live_view/etc.
  """
  defmacro __using__(which) when is_atom(which) do
    apply(__MODULE__, which, [])
  end
end
